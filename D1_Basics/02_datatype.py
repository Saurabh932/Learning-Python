# int - Interger
a= 5 
print(type(a))  # <class 'int'>

# float - Decimal Number
b= 1.2
print(type(b))   # <class 'float'>

# str - String
c = "Saurabh"
print(type(c))   # <class 'str'>

# bool - Boolean value (True/False)
print(type(5>6))  # <class 'bool'>

# None - Represents no value
print(type(None)) # <class 'NoneType'>